
(function () {
    const root = document.getElementById('nxAnnouncementStudio');
    const form = document.getElementById('nxAnnCreateForm');
    if (!root || !form) return;

    const templates = {
        custom: { name: 'مخصص', icon: '📣', category: 'عام', title: 'عنوان الإعلان', body: 'اكتب تفاصيل الإعلان ليتم عرض معاينة مباشرة هنا قبل النشر.', css: 'custom' },
        marriage: { name: 'زواج', icon: '💍', category: 'تهنئة', title: 'تهنئة بمناسبة الزواج', body: 'تتقدم الشركة بخالص التهاني والتبريكات إلى {person} بمناسبة الزواج. نتمنى له حياة سعيدة مليئة بالمودة والنجاح، ودوام الفرح والتوفيق.', css: 'marriage' },
        condolence: { name: 'وفاة', icon: '🕊️', category: 'تعزية', title: 'إنا لله وإنا إليه راجعون', body: 'تتقدم الشركة بخالص العزاء والمواساة إلى {person}. سائلين الله أن يتغمد الفقيد بواسع رحمته، وأن يلهم أهله وذويه الصبر والسلوان.', css: 'condolence' },
        newborn: { name: 'مولود جديد', icon: '👶', category: 'تهنئة', title: 'مبارك المولود الجديد', body: 'تتقدم الشركة بأصدق التهاني إلى {person} بمناسبة المولود الجديد {secondary}. نسأل الله أن يجعله من مواليد السعادة والبركة.', css: 'newborn' },
        holiday: { name: 'عطلة رسمية', icon: '📅', category: 'عطلة رسمية', title: 'عطلة رسمية', body: 'تعلن إدارة الشركة عن عطلة رسمية {date}. يرجى من جميع الموظفين الالتزام بالتعليمات الخاصة بالدوام حسب توجيهات الإدارة.', css: 'holiday' },
        promotion: { name: 'ترقية', icon: '📈', category: 'تهنئة', title: 'تهنئة بالترقية', body: 'نبارك إلى {person} ترقيته إلى {secondary}. نتمنى له دوام النجاح والتوفيق في مهامه الجديدة، مع الشكر والتقدير لجهوده المميزة.', css: 'promotion' },
        welcome: { name: 'ترحيب بموظف جديد', icon: '🤝', category: 'ترحيب', title: 'ترحيب بموظف جديد', body: 'نرحب بانضمام {person} إلى فريق العمل بمنصب {secondary}. نتمنى له بداية موفقة ومسيرة ناجحة ضمن عائلة الشركة.', css: 'welcome' },
        farewell: { name: 'وداع موظف', icon: '🧳', category: 'وداع', title: 'وداع وشكر', body: 'تتقدم الشركة بالشكر والتقدير إلى {person} على ما قدمه من جهود خلال فترة عمله. نتمنى له دوام التوفيق والنجاح في مسيرته القادمة.', css: 'farewell' }
    };

    const preview = document.getElementById('nxAnnPreview');
    const badge = document.getElementById('nxAnnPreviewBadge');
    const icon = document.getElementById('nxAnnPreviewIcon');
    const category = document.getElementById('nxAnnPreviewCategory');
    const title = document.getElementById('nxAnnPreviewTitle');
    const body = document.getElementById('nxAnnPreviewBody');
    const footer = document.getElementById('nxAnnPreviewFooter');
    const select = root.querySelector('[data-template-select]');

    const field = (name) => form.querySelector(`[data-ann-field="${name}"]`);
    const value = (name) => (field(name)?.value || '').trim();

    function selectedKey() {
        return root.querySelector('input[name="Announcement.TemplateKey"]:checked')?.value || 'custom';
    }

    function buildText(text) {
        const person = value('person') || 'زميلنا العزيز';
        const secondary = value('secondary') || 'المهمة الجديدة';
        const department = value('department') || 'قسم الموارد البشرية';
        const date = value('date') || '';
        return text
            .replaceAll('{person}', person)
            .replaceAll('{secondary}', secondary)
            .replaceAll('{department}', department)
            .replaceAll('{date}', date);
    }

    function markThumbs(key) {
        root.querySelectorAll('[data-ann-template-button]').forEach((btn) => {
            btn.classList.toggle('active', btn.dataset.annTemplateButton === key);
        });
    }

    function applyTemplate(key, forceText) {
        const template = templates[key] || templates.custom;
        const titleField = field('title');
        const bodyField = field('body');
        const categoryField = field('category');

        if (select && select.value !== key) select.value = key;
        markThumbs(key);

        if (categoryField && key !== 'custom') categoryField.value = template.category;
        if (titleField && key !== 'custom' && (forceText || !titleField.value.trim() || titleField.dataset.autoGenerated === 'true')) {
            titleField.value = template.title;
            titleField.dataset.autoGenerated = 'true';
        }
        if (bodyField && key !== 'custom' && (forceText || !bodyField.value.trim() || bodyField.dataset.autoGenerated === 'true')) {
            bodyField.value = buildText(template.body);
            bodyField.dataset.autoGenerated = 'true';
        }
        updatePreview();
    }

    function updatePreview() {
        const key = selectedKey();
        const template = templates[key] || templates.custom;
        const writtenTitle = value('title');
        const writtenBody = value('body');
        const department = value('department') || 'قسم الموارد البشرية';

        preview.className = `nxann-preview-canvas ${template.css}`;
        badge.textContent = template.name;
        icon.textContent = template.icon;
        category.textContent = field('category')?.value || template.category;
        title.textContent = writtenTitle || template.title;
        body.textContent = writtenBody || buildText(template.body);
        footer.textContent = department;
        markThumbs(key);
    }

    root.querySelectorAll('input[name="Announcement.TemplateKey"]').forEach((radio) => {
        radio.addEventListener('change', () => applyTemplate(radio.value, true));
    });

    root.querySelectorAll('[data-ann-template-button]').forEach((btn) => {
        btn.addEventListener('click', () => {
            const key = btn.dataset.annTemplateButton;
            const radio = root.querySelector(`input[name="Announcement.TemplateKey"][value="${key}"]`);
            if (radio) radio.checked = true;
            applyTemplate(key, true);
        });
    });

    if (select) {
        select.addEventListener('change', () => {
            const key = select.value;
            const radio = root.querySelector(`input[name="Announcement.TemplateKey"][value="${key}"]`);
            if (radio) radio.checked = true;
            applyTemplate(key, true);
        });
    }

    form.querySelectorAll('[data-ann-field]').forEach((input) => {
        input.addEventListener('input', () => {
            if (input.dataset.annField === 'body' || input.dataset.annField === 'title') {
                input.dataset.autoGenerated = 'false';
            }
            const key = selectedKey();
            const bodyField = field('body');
            if (bodyField && bodyField.dataset.autoGenerated === 'true' && key !== 'custom') {
                bodyField.value = buildText((templates[key] || templates.custom).body);
            }
            updatePreview();
        });
        input.addEventListener('change', updatePreview);
    });

    document.getElementById('nxAnnPreviewButton')?.addEventListener('click', updatePreview);

    const initial = selectedKey() === 'custom' ? 'condolence' : selectedKey();
    const initialRadio = root.querySelector(`input[name="Announcement.TemplateKey"][value="${initial}"]`);
    if (initialRadio) initialRadio.checked = true;
    applyTemplate(initial, true);
})();
