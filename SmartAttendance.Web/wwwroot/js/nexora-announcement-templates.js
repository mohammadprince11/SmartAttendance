
(function () {
    const root = document.getElementById('nxAnnouncementComposer');
    if (!root) return;

    const templates = {
        custom: { name: 'مخصص', icon: '📣', category: 'عام', title: 'عنوان الإعلان', body: 'اكتب تفاصيل الإعلان ليتم عرض معاينة مباشرة هنا قبل النشر.', css: 'custom' },
        marriage: { name: 'زواج', icon: '💍', category: 'تهنئة', title: 'تهنئة بمناسبة الزواج', body: 'تتقدم الشركة بخالص التهاني والتبريكات إلى {person} بمناسبة الزواج. نتمنى له حياة سعيدة مليئة بالمودة والنجاح.', css: 'marriage' },
        condolence: { name: 'وفاة', icon: '🕊️', category: 'تعزية', title: 'تعزية', body: 'تتقدم الشركة بخالص العزاء والمواساة إلى {person}. سائلين الله أن يتغمد الفقيد بواسع رحمته وأن يلهم أهله وذويه الصبر والسلوان.', css: 'condolence' },
        newborn: { name: 'مولود جديد', icon: '👶', category: 'تهنئة', title: 'مولود جديد', body: 'تتقدم الشركة بأصدق التهاني إلى {person} بمناسبة المولود الجديد {secondary}. نسأل الله أن يجعله من مواليد السعادة والبركة.', css: 'newborn' },
        holiday: { name: 'عطلة رسمية', icon: '📅', category: 'عطلة رسمية', title: 'عطلة رسمية', body: 'تعلن إدارة الشركة عن عطلة رسمية {date}. يرجى الالتزام بالتعليمات الخاصة بالدوام حسب توجيهات الإدارة.', css: 'holiday' },
        promotion: { name: 'ترقية', icon: '📈', category: 'تهنئة', title: 'تهنئة بالترقية', body: 'نبارك إلى {person} ترقيته إلى {secondary}. نتمنى له دوام النجاح والتوفيق في مهامه الجديدة.', css: 'promotion' },
        welcome: { name: 'ترحيب بموظف جديد', icon: '🤝', category: 'ترحيب', title: 'ترحيب بموظف جديد', body: 'نرحب بانضمام {person} إلى فريق العمل بمنصب {secondary}. نتمنى له بداية موفقة ومسيرة ناجحة ضمن عائلة الشركة.', css: 'welcome' },
        farewell: { name: 'وداع موظف', icon: '🧳', category: 'وداع', title: 'وداع وشكر', body: 'تتقدم الشركة بالشكر والتقدير إلى {person} على ما قدمه من جهود خلال فترة عمله. نتمنى له دوام التوفيق والنجاح في مسيرته القادمة.', css: 'farewell' }
    };

    const preview = document.getElementById('nxAnnPreview');
    const badge = document.getElementById('nxAnnPreviewBadge');
    const icon = document.getElementById('nxAnnPreviewIcon');
    const category = document.getElementById('nxAnnPreviewCategory');
    const title = document.getElementById('nxAnnPreviewTitle');
    const body = document.getElementById('nxAnnPreviewBody');
    const footer = document.getElementById('nxAnnPreviewFooter');
    const button = document.getElementById('nxAnnPreviewButton');

    const field = (name) => root.querySelector(`[data-ann-field="${name}"]`);
    const value = (name) => (field(name)?.value || '').trim();

    function currentTemplateKey() {
        return root.querySelector('input[name="Announcement.TemplateKey"]:checked')?.value || 'custom';
    }

    function applyTemplateToFields(key) {
        const template = templates[key] || templates.custom;
        const titleField = field('title');
        const bodyField = field('body');
        const categoryField = field('category');

        if (categoryField && key !== 'custom') categoryField.value = template.category;
        if (titleField && !titleField.value.trim()) titleField.value = template.title;
        if (bodyField && key !== 'custom' && (!bodyField.value.trim() || bodyField.dataset.autoGenerated === 'true')) {
            bodyField.value = buildText(template.body);
            bodyField.dataset.autoGenerated = 'true';
        }
        updatePreview();
    }

    function buildText(text) {
        const person = value('person') || 'زميلنا العزيز';
        const secondary = value('secondary') || '';
        const department = value('department') || 'قسم الموارد البشرية';
        const date = value('date') || '';
        return text
            .replaceAll('{person}', person)
            .replaceAll('{secondary}', secondary)
            .replaceAll('{department}', department)
            .replaceAll('{date}', date);
    }

    function updatePreview() {
        const key = currentTemplateKey();
        const template = templates[key] || templates.custom;
        const writtenTitle = value('title');
        const writtenBody = value('body');
        const department = value('department') || 'قسم الموارد البشرية';

        preview.className = `nxann-live-preview ${template.css}`;
        badge.textContent = template.name;
        icon.textContent = template.icon;
        category.textContent = field('category')?.value || template.category;
        title.textContent = writtenTitle || template.title;
        body.textContent = writtenBody || buildText(template.body);
        footer.textContent = department;
    }

    root.querySelectorAll('input[name="Announcement.TemplateKey"]').forEach((radio) => {
        radio.addEventListener('change', () => applyTemplateToFields(radio.value));
    });

    root.querySelectorAll('[data-ann-field]').forEach((input) => {
        input.addEventListener('input', () => {
            if (input.getAttribute('data-ann-field') === 'body') {
                input.dataset.autoGenerated = 'false';
            }
            const bodyField = field('body');
            const key = currentTemplateKey();
            if (bodyField && bodyField.dataset.autoGenerated === 'true' && key !== 'custom') {
                bodyField.value = buildText((templates[key] || templates.custom).body);
            }
            updatePreview();
        });
        input.addEventListener('change', updatePreview);
    });

    if (button) button.addEventListener('click', updatePreview);
    updatePreview();
})();

